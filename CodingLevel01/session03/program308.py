#Program to draw a pattern using squares or circles or combination of shapes using a while loop.

import turtle as bot

step = 150
i = 1
bot.speed(100)


while i <= 72:
    
    bot.forward(step)
    bot.right(51.43)
    bot.forward(step)
    bot.right(51.43)
    bot.forward(step)
    bot.right(51.43)
    bot.forward(step)
    bot.right(51.43)
    bot.forward(step)
    bot.right(51.43)
    bot.forward(step)
    bot.right(51.43)
    bot.forward(step)
    bot.right(51.43)
    i += 1
    bot.right(5)
    
   
    
    
